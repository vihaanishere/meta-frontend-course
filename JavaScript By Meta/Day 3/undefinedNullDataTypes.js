//Null Object
//Null is a primitive data type that represents the intentional absence of any object value. It is one of the falsy values in JavaScript, which means it evaluates to false in a boolean context. Null is often used to indicate that a variable has no value or that an object property is empty.
var letters = "abc";
console.log(letters.match("d"));

//Undefined Data Type
//Undefined is a primitive data type that represents the absence of a value. It is the default value of uninitialized variables and function parameters that are not provided with an argument. Undefined is also one of the falsy values in JavaScript.
var x;
console.log(x); 

//Empty String
var emptyString = "";
console.log(emptyString);
