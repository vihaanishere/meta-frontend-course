//Datatypes and Operators
//Assignment Operator (=)
//Used to assign values to variables
var x = 5;
//Arithmetic Operators (+, -, *, /, %)
/* 	- Addition (+)
	- Subtraction (-)
	- Division (/)
	- Multiply (*)
	- Power (**)
    - Remainder (%)
 */
var a = 10;
var b = 3;
console.log("Addition:", a + b);
console.log("Subtraction:", a - b);
console.log("Multiplication:", a * b);
console.log("Division:", a / b);
console.log("Power:", a ** b);
console.log("Remainder:", a % b);
//Comparison Operators (==, ===, !=, !==, >, <, >=, <=)
/* 	- Equal to (==) Note: Java only checks the value of the variables being compared not the data type hence (100 = "100") will be true
    - Strict Equal to (===) Note: Java checks both the value and the data type of the variables being compared hence (100 === "100") will be false
    - Not Equal to (!=)
    - Strict Not Equal to (!==)
    - Greater than (>)
    - Less than (<)
    - Greater than or equal to (>=)
    - Less than or equal to (<=)
    */
//Logical Operators (&&, ||, !)
/* 	-  AND (&&)
    -  OR (||)
    -  NOT (!)
    */

//Use of operator on strings and numbers
var str1 = "Hello";
var str2 = "World";
var num1 = 10;
var num2 = "20";
//String Concatenation
console.log(str1, str2);
console.log(str1, num1);
console.log(str1, num2);
console.log(num1, num2);
console.log(num1 + 1);
console.log(num2 + 1);
