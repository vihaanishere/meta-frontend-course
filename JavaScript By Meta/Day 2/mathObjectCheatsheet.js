//Math Object Cheatsheet
//The Math object is a built-in object that has properties and methods for mathematical constants and functions. It is not a function object.
//Math Object Properties
//Math Number Constants
//Math.PI - returns the value of PI
console.log(Math.PI);
//Math.E - returns the value of Euler's number
console.log(Math.E);
//Math.LN2 - returns the natural logarithm of 2
console.log(Math.LN2);
//Math Rounding Methods
//Math.round() - rounds a number to the nearest integer
console.log(Math.round(4.5));
console.log(Math.round(4.4));
//Math.ceil() - rounds a number up to the next largest integer
console.log(Math.ceil(4.1));
console.log(Math.ceil(4.9));
//Math.floor() - rounds a number down to the next smallest integer
console.log(Math.floor(4.9));
console.log(Math.floor(4.1));
//Math.trunc() - rounds a number towards zero
console.log(Math.trunc(4.9));
console.log(Math.trunc(-4.9));
//Math Arithmetic Methods
//Math.pow() - returns the value of a base raised to the power of an exponent
console.log(Math.pow(2, 3)); //2 raised to the power of 3
//Math.sqrt() - returns the square root of a number
console.log(Math.sqrt(16));
//Math.abs() - returns the absolute value of a number
console.log(Math.abs(-5));
//Math.max() - returns the largest of zero or more numbers
console.log(Math.max(1, 2, 3, 4, 5));
//Math.min() - returns the smallest of zero or more numbers
console.log(Math.min(1, 2, 3, 4, 5));
//Math Trigonometric Methods
//Math.sin() - returns the sine of a number
console.log(Math.sin(Math.PI / 2)); //sine of 90 degrees
//Math.cos() - returns the cosine of a number
console.log(Math.cos(0)); //cosine of 0 degrees
//Math.tan() - returns the tangent of a number
console.log(Math.tan(Math.PI / 4)); //tangent of 45 degrees

//Math Random Methods
//Math.random() - returns a random number between 0 (inclusive) and 1 (exclusive)
console.log(Math.random());


